public class Loop {
    public static void main(String[] args) {
        int i = 1 ;
        //while loop
        while(i <= 10) {
            System.out.println(i);
            i++;
        }
        
        // do-while loop
        int b = 11;
        do {
            System.out.println(b);
            b++;
        } while(b <= 10);

        // menu item selection --> do while 
        /*
        1.play game 
        2. return saved ame 
        3.exit 
        */
        
        // for loop
        for  (int m = 1; m <= 10; m++) {
            System.out.println(m);
        }
        
        
    }
}
